<section>
    <h2 class="section-title">So how does Unda help small channels?</h2>
    <p>
        Unlike most video sharing sites, Unda aims to garner as many viewers as possible for content
        creators without the need of third-party social media sites. A newly published video will be
        placed into a newsfeed for at most 24 hours where the Unda algorithm splits the content fairly
        to avoid saturation and gain exposure for all videos.
    </p>
    <div class='wrap'>
        <div class="process">
            <h3 class="number">
                <span>1</span>
            </h3>
            Upload & publish new video
        </div>
        <div class="process">
            <h3 class="number">
                <span>2</span>
            </h3>
            Video placed in news feed for 24 hours or until it hits the 5000 view cap
        </div>
        <div class="process">
            <h3 class="number">
                <span>3</span>
            </h3>
            View cap increases by 5 views for every like it recieves
        </div>
    </div>
    <br>
    <br>
    <p>
        Once the video is removed from the news feed it is simply hosted like any other video
        and can be viewed at any time as long as it is public. New videos can also trend but
        are held to different standards due to the boost they recieve.
    </p>
    <br>
